package com.jebert.sample.taskmanager.worker;

public class IllegalAssignment extends Exception {

	public IllegalAssignment() {
	}

	public IllegalAssignment(String arg0) {
		super(arg0);
	}

	public IllegalAssignment(Throwable arg0) {
		super(arg0);
	}

	public IllegalAssignment(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
